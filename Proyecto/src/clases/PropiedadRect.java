package clases;

public class PropiedadRect {
    private int iniciax;
    private int iniciay;
    private int ancho;
    private int largo;

    public PropiedadRect(int iniciax, int iniciay, int ancho, int largo) {
        this.iniciax = iniciax;
        this.iniciay = iniciay;
        this.ancho = ancho;
        this.largo = largo;
    }

    public int getIniciax() {
        return iniciax;
    }

    public void setIniciax(int iniciax) {
        this.iniciax = iniciax;
    }

    public int getIniciay() {
        return iniciay;
    }

    public void setIniciay(int iniciay) {
        this.iniciay = iniciay;
    }

    public int getAncho() {
        return ancho;
    }

    public void setAncho(int ancho) {
        this.ancho = ancho;
    }

    public int getLargo() {
        return largo;
    }

    public void setLargo(int largo) {
        this.largo = largo;
    }

   
    
}
